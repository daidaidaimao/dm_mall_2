let base = '';

export const testRequest = (url,params) => {
    return axios({
        method: 'post',
        url: `${base}${url}`,
        data:params,
        transformRequest : [function(data){
            return Qs.stringify(data)
        }],
        headers: {
            'Content-Type': 'application/x-www-form-urlencoded'  //必须设置传输方式
         }
    });
}




